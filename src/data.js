const obteniendodatapoke = (mostradata) => {
  arraydatapokemon = [];
  for (let i = 0; i < mostradatapokemon.length; i++); {
    arraydatapokemon.push({
      identificacion: mostradata[i].id,
      imagenes: mostradata[i].img,
      nombre: mostradata[i].name,
    });
  }
  return arraydatapokemon;
};


